package ltd.newbee.mall.entity;

public class ResDetailScore {
	private float star;
	private String distribute;
	private long scoreNum;
	private float avgTaste;
	private float avgService;
	private float avgAtmosphere;
	private float avgCost;
	private float avgDrink;
	private long totalReview;
	
	public float getStar() {
		return star;
	}
	public void setStar(float star) {
		this.star = star;
	}
	public String getDistribute() {
		return distribute;
	}
	public void setDistribute(String distribute) {
		this.distribute = distribute;
	}
	public long getScoreNum() {
		return scoreNum;
	}
	public void setScoreNum(long scoreNum) {
		this.scoreNum = scoreNum;
	}
	public float getAvgTaste() {
		return avgTaste;
	}
	public void setAvgTaste(float avgTaste) {
		this.avgTaste = avgTaste;
	}
	public float getAvgService() {
		return avgService;
	}
	public void setAvgService(float avgService) {
		this.avgService = avgService;
	}
	public float getAvgAtmosphere() {
		return avgAtmosphere;
	}
	public void setAvgAtmosphere(float avgAtmosphere) {
		this.avgAtmosphere = avgAtmosphere;
	}
	public float getAvgCost() {
		return avgCost;
	}
	public void setAvgCost(float avgCost) {
		this.avgCost = avgCost;
	}
	public float getAvgDrink() {
		return avgDrink;
	}
	public void setAvgDrink(float avgDrink) {
		this.avgDrink = avgDrink;
	}
	public long getTotalReview() {
		return totalReview;
	}
	public void setTotalReview(long totalReview) {
		this.totalReview = totalReview;
	}
	
	
	
}
